package com.unacademy;

public class switch_1 {

    public static void main(String[] args) {
        char color = 'A';
        switch(color) {
            case 'A':
                System.out.println("A");
                break;
            case 'B':
                System.out.println("B");
                break;
            default:
                System.out.println("No color");
        }
    }
}
